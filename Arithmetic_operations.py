# Arithmetic Operations in Python

num1 = 12
num2 = 12

# Addition
sum_result = num1 + num2
print("Sum:", sum_result)

# Subtraction
sub = num1 - num2
print("Subtraction:", sub)

# Multiplication
mul = num1 * num2
print("Multiplication:", mul)

# Division
div = num1 / num2
print("Division:", div)

# Floor Division
floor_division = num1 // num2
print("Floor Division:", floor_division)

# Modulus
mod = num1 % num2
print("Modulus:", mod)

# Exponent
exp = num1 ** num2
print("Exponent:", exp)
